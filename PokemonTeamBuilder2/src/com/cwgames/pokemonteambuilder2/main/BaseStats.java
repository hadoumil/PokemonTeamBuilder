package com.cwgames.pokemonteambuilder2.main;

public class BaseStats {

    public int hp;
    public int atk;
    public int def;
    public int spa;
    public int spd;
    public int spe;
    
    public BaseStats(int hp, int atk, int def, int spa, int spd, int spe) {
		this.hp = hp;
		this.atk = atk;
		this.def = def;
		this.spa = spa;
		this.spd = spd;
		this.spe = spe;
    }
    
}
